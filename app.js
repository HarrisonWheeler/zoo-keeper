
class Cat {
  constructor(name, color, gender, weight, sound, vegetarian, species) {
    this.name = name;
    this.color = color;
    this.gender = gender;
    this.weight = weight;
    this.sound = sound;
    this.vegetarian = vegetarian;
    this.species = species;
  }
}

